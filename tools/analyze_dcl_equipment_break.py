def dispatch_target(pe: pefile.PE, raw: bytes, formula_id: int) -> int:
    entry = dispatch.rva_bytes(pe, raw, DISPATCH_TABLE_RVA + formula_id * 8, 8)
    return struct.unpack("<Q", entry)[0] - pe.OPTIONAL_HEADER.ImageBase


def catalog_ids(path: Path) -> dict[int, tuple[int, ...]]:
    found: dict[int, list[int]] = {formula: [] for formula in TARGETS}
    with path.open(encoding="utf-8-sig", newline="") as handle:
        for row in csv.DictReader(handle):
            value = row.get("formula_hex", "").upper()
            for formula in TARGETS:
                if value == f"0X{formula:02X}":
                    found[formula].append(int(row["id_dec"]))
    return {formula: tuple(ids) for formula, ids in found.items()}


def render(exe: Path, catalog_path: Path, output: Path) -> tuple[str, bool]:
    raw = exe.read_bytes()
    pe = pefile.PE(data=raw, fast_load=True)
    actual_targets = {formula: dispatch_target(pe, raw, formula) for formula in TARGETS}
    actual_ids = catalog_ids(catalog_path)

    anchor_rows: list[tuple[Anchor, bool, str]] = []
    for anchor in ANCHORS:
        expected = bytes.fromhex(anchor.expected)
        actual = dispatch.rva_bytes(pe, raw, anchor.rva, len(expected))
        anchor_rows.append((anchor, actual == expected, " ".join(f"{value:02X}" for value in actual)))

    lines = [
        "# DCL equipment-break transaction analysis", "",
        "Generated by `tools/analyze_dcl_equipment_break.py`.", "",
        "## Inputs", "",
        f"- Executable: `{exe}`",
        f"- SHA-256: `{hashlib.sha256(raw).hexdigest().upper()}`",
        f"- Ability catalog: `{catalog_path}`",
        f"- Output: `{output}`", "",
        "## Dispatch and catalog", "",
        "| Formula | Handler | Expected | Catalog ids | Expected ids | Result |",
        "| --- | ---: | ---: | --- | --- | --- |",
    ]
    for formula, expected_target in TARGETS.items():
        passed = actual_targets[formula] == expected_target and actual_ids[formula] == EXPECTED_IDS[formula]
        lines.append(
            f"| `0x{formula:02X}` | `0x{actual_targets[formula]:X}` | `0x{expected_target:X}` | "
            f"`{actual_ids[formula]}` | `{EXPECTED_IDS[formula]}` | {'PASS' if passed else 'FAIL'} |"
        )

    lines.extend(["", "## Byte anchors", "", "| Name | RVA | Result | Meaning |", "| --- | ---: | --- | --- |"])
    for anchor, passed, actual in anchor_rows:
        result = "PASS" if passed else f"FAIL (`{actual}`)"
        lines.append(f"| `{anchor.name}` | `0x{anchor.rva:X}` | {result} | {anchor.meaning} |")

    lines.extend([
        "", "## Static interpretation", "",
        "- **Strong:** formulas `0x25` and `0x2E` share the exact-id equipment selector already used",
        "  by Steal/Plunder. A valid selection stages the item at `result+0x04`, its concrete slot mask",
        "  at `result+0x1B`, and returns zero.",
        "- **Strong:** successful native equipment destruction is carried by `result+0x12` bit",
        "  `0x0004`. Formula `0x2E` retains its ordinary physical damage chain after staging that bit.",
        "- **Strong:** formula `0x25` is re-entrant: without matching equipment it temporarily replaces",
        "  the outer order with Basic Attack `(type=1,id=0)`, calls the universal calculation entry,",
        "  then restores the Rend order. Repointing the ability away from `0x25` removes this nested",
        "  provenance hazard.",
        "- **Strong:** the DCL Knight definition rejects permanent equipment loss in favor of reversible",
        "  Guard Break and Bind Weapon. The data-first implementation is to repoint the reused action",
        "  records to an ordinary authored physical/status formula. If a native break formula must be",
        "  retained for presentation, clear result bit `0x0004` at the post-calculation boundary before",
        "  the protected side-effect applicator; preserve or replace the numeric damage independently.",
        "- **Live gate:** native destruction cardinality matters only for abilities intentionally left on",
        "  the vanilla break transaction. The DCL replacement itself needs physical/status integration",
        "  tests, not a reverse-engineering test of permanent inventory loss.", "",
    ])

    dispatch_ok = all(actual_targets[f] == TARGETS[f] and actual_ids[f] == EXPECTED_IDS[f] for f in TARGETS)
    ok = dispatch_ok and all(passed for _, passed, _ in anchor_rows)
    return "\n".join(lines), ok


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--exe", type=Path, default=DEFAULT_EXE)
    parser.add_argument("--catalog", type=Path, default=DEFAULT_CATALOG)
    parser.add_argument("--output", type=Path)
    args = parser.parse_args()
    if not args.exe.exists():
        raise SystemExit(f"executable not found: {args.exe}")
    if not args.catalog.exists():
        raise SystemExit(f"ability catalog not found: {args.catalog}")
    output = args.output or ROOT / "work" / f"{int(time.time())}-dcl-equipment-break-analysis.md"
    report, ok = render(args.exe, args.catalog, output)
    output.write_text(report, encoding="utf-8", newline="\n")
    print(f"wrote {output}")
    print("all equipment-break checks PASS" if ok else "one or more equipment-break checks FAIL")
    return 0 if ok else 1


if __name__ == "__main__":
    raise SystemExit(main())

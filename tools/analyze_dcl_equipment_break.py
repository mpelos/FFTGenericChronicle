DISPATCH_TABLE_RVA = 0x682BC8
TARGETS = {0x25: 0x307E70, 0x2E: 0x308534}
EXPECTED_IDS = {0x25: (138, 139, 140, 141), 0x2E: (160, 161, 162, 163)}



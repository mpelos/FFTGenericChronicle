#!/usr/bin/env python3
"""Verify numeric result-flag writers and selector priority in the current Enhanced executable."""
from __future__ import annotations

import argparse
import hashlib
import time
from dataclasses import dataclass
from pathlib import Path

import pefile


ROOT = Path(__file__).resolve().parents[1]
DEFAULT_EXE = Path(
    r"D:/SteamLibrary/steamapps/common/FINAL FANTASY TACTICS - The Ivalice Chronicles/FFT_enhanced.exe"
)


@dataclass(frozen=True)
class Anchor:
    name: str
    rva: int
    expected_hex: str
    meaning: str

    @property
    def expected(self) -> bytes:
        return bytes.fromhex(self.expected_hex)


ANCHORS = (
    Anchor("hp-credit-writer", 0x3060C9, "66 89 50 08 C6 40 27 40", "stages HP credit and exact flag 0x40"),
    Anchor("hp-debit-writer", 0x3060EA, "66 89 48 06 C6 40 27 80", "stages HP debit and exact flag 0x80"),
    Anchor("mp-debit-writer", 0x3063CB, "66 89 59 06 C6 41 27 20", "stages MP debit and exact flag 0x20"),
    Anchor("mp-credit-writer", 0x306BFC, "66 89 48 0C C6 40 27 10", "stages MP credit and exact flag 0x10"),
    Anchor("combined-credit-writer-a", 0x272355, "C6 47 27 50", "native combined HP+MP credit flag 0x50"),
    Anchor("combined-credit-writer-b", 0x3088C5, "41 C6 41 27 50", "second native combined HP+MP credit flag 0x50"),
    Anchor("damage-mp-credit-writer-a", 0x308C8A, "C6 42 27 90", "native HP-debit+MP-credit flag 0x90"),
    Anchor("damage-mp-credit-writer-b", 0x309084, "C6 43 27 90", "second native HP-debit+MP-credit flag 0x90"),
    Anchor("selector-read-and-damage-first", 0x205286, "8A 87 E5 01 00 00 84 C0 79 36", "reads unit+0x1E5 and selects signed/0x80 HP damage first"),
    Anchor("selector-mp-debit-second", 0x2052C6, "A8 20 0F 85 21 01 00 00", "selects bit 0x20 after no HP-damage bit"),
    Anchor("selector-credit-route", 0x20536E, "A8 50 75 72", "routes either 0x40 or 0x10 through the shared credit branch"),
    Anchor("selector-credit-kind", 0x2053E4, "C7 03 16 00 00 00", "commits selector kind 0x16 for the credit route"),
    Anchor("selector-mp-kind", 0x2053EF, "C7 03 0F 00 00 00", "commits selector kind 0x0F for the MP-debit route"),
)


def render(exe: Path, output: Path) -> tuple[str, bool]:
    raw = exe.read_bytes()
    pe = pefile.PE(data=raw, fast_load=True)
    checks: list[tuple[Anchor, bool]] = []
    for anchor in ANCHORS:
        offset = pe.get_offset_from_rva(anchor.rva)
        actual = raw[offset : offset + len(anchor.expected)]
        checks.append((anchor, actual == anchor.expected))
    ok = all(passed for _, passed in checks)

    lines = [
        "# DCL numeric result-flag analysis",
        "",
        "Generated by `tools/analyze_dcl_result_flags.py`.",
        "",
        "## Inputs",
        "",
        f"- Executable: `{exe}`",
        f"- SHA-256: `{hashlib.sha256(raw).hexdigest().upper()}`",
        f"- Output: `{output}`",
        "",
        "## Anchor verification",
        "",
        "| Anchor | RVA | Result | Meaning |",
        "| --- | ---: | --- | --- |",
    ]
    for anchor, passed in checks:
        lines.append(
            f"| `{anchor.name}` | `0x{anchor.rva:X}` | {'PASS' if passed else 'FAIL'} | {anchor.meaning} |"
        )

    lines.extend(
        [
            "",
            "## Native contract",
            "",
            "The high nibble of `unit+0x1E5` is a bitwise numeric-result bundle:",
            "",
            "- `0x80`: HP debit;",
            "- `0x40`: HP credit;",
            "- `0x20`: MP debit;",
            "- `0x10`: MP credit.",
            "",
            "Native writers emit combined `0x50` (HP+MP credit) and `0x90` (HP debit+MP credit),",
            "which proves that the field is not a single exclusive enum. The selector reads the",
            "bundle with a fixed primary-presentation priority: HP debit first, MP debit second,",
            "low-bit effects next, and the shared HP/MP credit route last.",
            "",
            "A partial Mana Shield split therefore has the structural bundle `0xA0`. The shared",
            "HP/MP apply routine consumes both staged numbers independently, and the selector chooses",
            "HP damage as the primary presentation. There is no verified literal native `0xA0` writer;",
            "its numeric application is Strong from the bitwise contract, while the exact popup plus",
            "Mana Shield animation combination remains a live presentation gate.",
            "",
            "## Runtime consequence",
            "",
            "`DclResultFlagsControlEnabled` rebuilds only the numeric high nibble from the final four",
            "staged channels and preserves configured low effect bits. The flag write is the last",
            "member of the existing transactional channel write, so a failure rolls the earlier HP/MP",
            "writes back before the callback fails open.",
            "",
        ]
    )
    return "\n".join(lines), ok


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--exe", type=Path, default=DEFAULT_EXE)
    parser.add_argument("--output", type=Path)
    parser.add_argument("--check-only", action="store_true")
    args = parser.parse_args()
    output = args.output or ROOT / "work" / f"{int(time.time())}-dcl-result-flags-analysis.md"
    report, ok = render(args.exe, output)
    if not args.check_only:
        output.write_text(report, encoding="utf-8", newline="\n")
        print(f"wrote {output}")
    print("all numeric result-flag anchors PASS" if ok else "one or more numeric result-flag anchors FAIL")
    return 0 if ok else 1


if __name__ == "__main__":
    raise SystemExit(main())
